using System;
using System.Data;
using System.Configuration;
using System.Web;
using System.Xml.Serialization;

namespace PaladinPharmacyCOMv1.Models
{
    public class RxPayment
    {
        //---------------------------------------------------------------------------------------------------------
        //---------------------------------------------------------------------------------------------------------

        private int m_Type;
        private decimal m_Amount;
        private string m_Message;

        //---------------------------------------------------------------------------------------------------------
        //---------------------------------------------------------------------------------------------------------

        [XmlAttribute()]
        public int Type
        {
            get { return m_Type; }
            set { m_Type = value; }
        }

        //---------------------------------------------------------------------------------------------------------

        [XmlAttribute()]
        public decimal Amount
        {
            get { return m_Amount; }
            set { m_Amount = value; }
        }

        //---------------------------------------------------------------------------------------------------------

        [XmlAttribute()]
        public string Message
        {
            get { return m_Message; }
            set { m_Message = value; }
        }

        //---------------------------------------------------------------------------------------------------------
        //---------------------------------------------------------------------------------------------------------

        public RxPayment() { }

        public RxPayment(int type, decimal amt, string message)
        {
            m_Type = type;
            m_Amount = amt;
            m_Message = message;
        }

        //---------------------------------------------------------------------------------------------------------
        //---------------------------------------------------------------------------------------------------------
    }
}
