using System;
using System.Data;
using System.Configuration;
using System.Web;
using System.Xml.Serialization;

namespace PaladinPharmacyCOMv1.Models
{
    public class RxMessage
    {
        //---------------------------------------------------------------------------------------------------------
        //---------------------------------------------------------------------------------------------------------

        private string m_message;
        private bool m_print;

        //---------------------------------------------------------------------------------------------------------
        //---------------------------------------------------------------------------------------------------------

        [XmlAttribute()]
        public string Message
        {
            get { return m_message; }
            set { m_message = value; }
        }

        //---------------------------------------------------------------------------------------------------------

        [XmlAttribute()]
	    public bool Print
	    {
		    get { return m_print;}
		    set { m_print = value;}
	    }

        //---------------------------------------------------------------------------------------------------------
        //---------------------------------------------------------------------------------------------------------

        public RxMessage() { }
        public RxMessage(string msg)
        {
            m_message = msg;
            m_print = false;
        }
        public RxMessage(string msg, bool printMsg)
        {
            m_message = msg;
            m_print = printMsg;

        }

        //---------------------------------------------------------------------------------------------------------
        //---------------------------------------------------------------------------------------------------------
    }
}
